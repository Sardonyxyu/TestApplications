package me.sardonyxyu.test.testapplication;

import android.os.Bundle;

import com.amap.api.navi.AMapNaviView;
import com.amap.api.navi.enums.NaviType;
import com.amap.api.navi.model.NaviLatLng;

public class WalkNavigationActivity extends BaseActivity {
    private double latitude;
    private double longitude;
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_walk_navigation);
        mAMapNaviView = (AMapNaviView) findViewById(R.id.navi_view);
        mAMapNaviView.onCreate(savedInstanceState);
        mAMapNaviView.setAMapNaviViewListener(this);
    }

    @Override
    public void onInitNaviSuccess() {
        super.onInitNaviSuccess();
        latitude = getIntent().getDoubleExtra("latitude" , 22.560555);
        longitude = getIntent().getDoubleExtra("longitude" , 113.898139);
        mAMapNavi.calculateWalkRoute(new NaviLatLng(latitude, longitude), new NaviLatLng(22.560555, 113.898139));

    }

    @Override
    public void onCalculateRouteSuccess() {
        super.onCalculateRouteSuccess();
        mAMapNavi.startNavi(NaviType.GPS);
    }
}
